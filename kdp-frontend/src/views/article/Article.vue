<template>
  <Loader v-if="loading" />
  <div class="content-container" v-else>
    <div class="article-header-container">
      <div class="article-title-container">
        <h1>{{ articleDetails.title }}</h1>
      </div>
      <ProfileTag
        :name="getRockstar.name"
        :image="getRockstar.image"
      ></ProfileTag>
    </div>
  </div>
  <div class="background-container">
    <div class="article-details">
      <div class="publish-date">
        <p>{{ customPublishDate }}</p>
      </div>
    </div>
    <div class="content-container article-content">
      <Blog :articleContent="articleDetails.content" />
    </div>
  </div>
</template>

<script lang="ts">
import { useRoute } from "vue-router";
import { useStore } from "vuex";
import { computed, onMounted } from "vue";

import ArticleShape from "@/models/Article";
import { RockstarShape } from "@/models/Rockstar";

import Blog from "./components/Blog.vue";
import Loader from "@/components/loader/Loader.vue";
import ProfileTag from "@/components/profileTag/Profiletag.vue";

export default {
  name: "Article-view",
  components: {
    Blog,
    ProfileTag,
    Loader,
  },
  setup() {
    const route = useRoute();
    const store = useStore();

    const loading = computed(() => store.getters["isLoading"]);

    const articleId = computed(() => {
      return route.params.articleId;
    });

    onMounted(() => {
      store.commit("article/CLEAR_ARTICLE");
      store
        .dispatch("article/getArticle", articleId.value)
        .then(() => store.dispatch("article/getRockstar"));
    });

    const articleDetails = computed((): ArticleShape => {
      const article = store.getters["article/getArticle"];
      return article;
    });

    const customPublishDate = computed(() => {
      const publishDate = articleDetails.value.publishDate;
      const language = navigator.language;
      const publishDateCustom = new Date(
        publishDate.toString()
      ).toLocaleDateString(language);
      return publishDateCustom;
    });

    const getRockstar = computed((): RockstarShape => {
      return store.getters["article/getRockstar"];
    });

    return {
      articleId,
      articleDetails,
      loading,
      getRockstar,
      customPublishDate,
    };
  },
};
</script>

